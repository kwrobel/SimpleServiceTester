package com.example.simpleservicetester.entity;

import javax.xml.bind.annotation.XmlRootElement;

/**
 * A single post submitted by a user containing a title and body
 * @author Kay Wrobel
 */
@XmlRootElement
public class Post {
    
    private int userId;
    private int id;
    private String title;
    private String body;

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }
    
}
